        /// The dash dot dot.
        /// </summary>
        DashDotDot, 

        /// <summary>
        /// The dash dash dot dot.
        /// </summary>
        DashDashDotDot, 

        /// <summary>
        /// The long dash.
        /// </summary>
        LongDash, 

        /// <summary>
        /// The long dash dot.
        /// </summary>
        LongDashDot, 

        /// <summary>
        /// The long dash dot dot.
        /// </summary>
        LongDashDotDot, 

        /// <summary>
        /// The none.
        /// </summary>
        None
    }
}
