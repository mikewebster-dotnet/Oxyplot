    /// The line style.
    /// </summary>
    public enum LineStyle
    {
        /// <summary>
        /// The solid.
        /// </summary>
        Solid, 

        /// <summary>
        /// The dash.
        /// </summary>
        Dash, 

        /// <summary>
        /// The dot.
        /// </summary>
        Dot, 

        /// <summary>
        /// The dash dot.
        /// </summary>
        DashDot, 

        /// <summary>
        /// The dash dash dot.
        /// </summary>
        DashDashDot, 

