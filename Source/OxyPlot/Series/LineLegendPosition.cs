namespace OxyPlot
{
    /// <summary>
    /// Specifies the position of legends rendered on a line series.
    /// </summary>
    public enum LineLegendPosition
    {
        /// <summary>
        ///   No legend on the line.
        /// </summary>
        None, 

        /// <summary>
        ///   Legend at the start of the line.
        /// </summary>
        Start, 

        /// <summary>
        ///   Legend at the end of the line.
        /// </summary>
        End
    }
}